def calculate_square_area(side_length):
    area = side_length ** 2
    return area

# Example usage:
side_length_example_1 = 7
result_1 = calculate_square_area(side_length_example_1)
print(f"If the side length is {side_length_example_1}, the area of the square is: {result_1}")

side_length_example_2 = 3
result_2 = calculate_square_area(side_length_example_2)
print(f"If the side length is {side_length_example_2}, the area of the square is: {result_2}")
